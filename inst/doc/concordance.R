## ----setup, include = FALSE---------------------------------------------------------------------------------
library(concordance)
knitr::opts_chunk$set(
  collapse = TRUE,
  comment = "#>"
)

## -----------------------------------------------------------------------------------------------------------
f <- function(x)
  x[1]^2 + x[1]*x[2] + x[2]^3/9

## -----------------------------------------------------------------------------------------------------------
n <- 500
x <- matrix(runif(3*n), nrow=n)
y <- apply(x, 1, f)

## -----------------------------------------------------------------------------------------------------------
library(BASS)
mod <- bass(x, y)

## -----------------------------------------------------------------------------------------------------------
C <- C_bass(mod)
round(C*45, 3)

## -----------------------------------------------------------------------------------------------------------
eigen(C)

## -----------------------------------------------------------------------------------------------------------
act_scores(C)
act_scores(C, k=3)

